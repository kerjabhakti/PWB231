    $b = 2;

    // Operator Aritmatika
    echo "Hasil dari $a + $b adalah " . ($a + $b) . "<br>"; // Penjumlahan
    echo "Hasil dari $a - $b adalah " . ($a - $b) . "<br>"; // Pengurangan
    echo "Hasil dari $a * $b adalah " . ($a * $b) . "<br>"; // Perkalian
    echo "Hasil dari $a / $b adalah " . ($a / $b) . "<br>"; // Pembagian
    echo "Hasil dari $a % $b adalah " . ($a % $b) . "<br>"; // Sisa Pembagian
    echo "Hasil dari $a ++ $b adalah " . ($a ++ ) . "<br>"; // Penaikan
    echo "Hasil dari $a -- $b adalah " . ($a -- ) . "<br>"; // Penurunan
    ?>
